#include "libft.h"

/*
**	Производит побитовый реверс типа unsigned int.
**
**	Пример:	1100 1010 | 1010 0011
*/

void	ft_reverse_uint(unsigned int *n)
{
	*n = ((*n << 24) |
		  (*n >> 24) |
		  ((*n >> 16) << 24) >> 16 |
		  ((*n >> 8) << 24) >> 8);
}
